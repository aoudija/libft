#include "libft.h"

int	ft_atoi(const char *str)
{
	int	s;
	int	nb;

	nb = 0;
	s = 1;
	while ((*str > 9 && *str <= 13) || *str == 32)
		str++;
	if (*str == '-')
		s *= -1;
	if (*str == '-' || *str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		nb = nb * 10 + *str - '0';
		str++;
	}
	return (nb * s);
}

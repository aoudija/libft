#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	int		i;
	char	*s1;

	s1 = (char *) s;
	i = 0;
	while (s1[i] != c && i < n)
	{
		i++;
	}
	return (s1 + i);
}
